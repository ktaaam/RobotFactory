        <h3>Cute household robots</h3>
        <p>Umbrella Corp currently produces six robot models, each designated by a single letter. 
            There are three lines of robots - household, butler and companion bots, 
            in order of perceived value and increasing price. 
            Models A through L have been reserved for household bots, 
            M through V for butler bots, and W through Z for companion bots.</p>
        <p>Robots come in three pieces - top (with head and manipulator appendages), 
            torso (for essential fluids and computational components) and bottom (with propulsion mechanisms). 
            Each piece has a two-letter designation - the bot model (a-z) and the part (1-3). 
            You should grab the collection of parts pictures to use in your app</p>
        <p>Each plant has an inventory of robot parts, specializes in making one piece for a designated robot model, 
            and can ship unneeded parts or assembled robots to Umbrella's Panda Research Center, for credit.</p>
        <p>Each robot piece, or assembled robot, has a Certificate of Authenticity (CA) code, issued by Umbrella Corp. 
            When a plant buys parts from the research center, each comes with its own CA. 
            When a plant sells parts or robots, they surrender the CA(s) for each of the pieces.</p>
